package opinion;

public class Review {
	private String login;
	private String title;
	private float mark;
	private String comment;

	public String getLogin() {
		return this.login;
	}
	
	public String getTitle() {
		return this.title;
	}
	
	public float getMark() {
		return this.mark;
	}
	
	public String getComment() {
		return this.comment;
	}
}